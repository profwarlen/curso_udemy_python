# Casting, ou conversão de tipos de dados, é o processo de alterar o tipo de dado de uma variável para outro tipo. Em Python, você pode realizar o casting de dados usando funções embutidas específicas para cada tipo de dado.

# Aqui estão alguns exemplos de casting de dados em Python:

# - Casting para `int` (inteiro):
  
x = 3.14
y = int(x)


# - Casting para `float` (ponto flutuante):

x = 5
y = float(x)

# - Casting para `str` (string):

x = 10
y = str(x)


# É importante observar que nem todas as conversões de tipos são possíveis ou fazem sentido. Por exemplo, converter uma string que não representa um número em um inteiro resultará em um erro. Portanto, é necessário ter cuidado ao realizar o casting de dados e garantir que a conversão seja válida para evitar erros ou comportamentos inesperados em seu código.